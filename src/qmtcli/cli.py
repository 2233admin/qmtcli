def _connect(args: argparse.Namespace) -> QMTGateway:
    account_optional = getattr(args, "command", None) in ACCOUNT_OPTIONAL_COMMANDS
    if not args.account and not account_optional:
        raise SystemExit("--account is required for this command")
    gateway = QMTGateway(QMTConnection(args.path or "", args.account or "", args.account_type))
    gateway.connect(subscribe=not account_optional)
    return gateway


def main(argv: list[str] | None = None) -> int:
    # Windows defaults stdout to the ANSI code page (GBK on zh-CN); JSON output is UTF-8.
    if hasattr(sys.stdout, "reconfigure"):
        sys.stdout.reconfigure(encoding="utf-8")
    args = build_parser().parse_args(argv)
    try:
        return _run_command(args)
    except Exception as exc:  # noqa: BLE001 - CLI boundary must report arbitrary failures cleanly.
        _print_json({"ok": False, "error": str(exc)})
        return 1


def _run_command(args: argparse.Namespace) -> int:
    if getattr(args, "xtdc_token", ""):
        QMTGateway.init_xtdc(args.xtdc_token, args.xtdc_port)
    if args.command == "rpc":
        return _rpc()
    if args.command == "server":
        return _server()
    if args.command == "capabilities":
        _print_json(_agent_response(AGENT_CAPABILITIES))
        return 0
    if args.command == "schema":
        _print_json(_agent_response(AGENT_SCHEMA))
        return 0
    if args.command == "examples":
        _print_json(_agent_response(AGENT_EXAMPLES))
        return 0
    if args.command == "status":
        _print_json({"xtquant": QMTGateway.is_available(args.path)})
        return 0
    if args.command == "doctor":
        _print_json(QMTGateway.sdk_diagnostics(args.path, args.account))
        return 0
    if args.command == "fields":
        _print_json(_dispatch_fields_command(args.kind))
        return 0
    if args.command in {
        "calendar",
        "sector-list",
        "sector-stocks",
        "full-tick",
        "bars",
        "l2-quote",
        "l2-order",
        "l2-transaction",
        "data-call",
        "instrument-detail",
        "instrument-type",
        "trading-dates",
        "divid-factors",
        "holidays",
        "period-list",
        "ipo-info",
        "cb-info",
        "etf-info",
        "index-weight",
        "financials",
        "download",
    }:
        _print_json(_handle_data_command(args))
        return 0

    gateway = _connect(args)
    if args.command == "asset":
        _print_json(gateway.query_asset())
    elif args.command == "positions":
        _print_json(gateway.query_positions())
    elif args.command == "orders":
        _print_json(gateway.query_orders(cancelable_only=args.cancelable_only))
    elif args.command == "trades":
        _print_json(gateway.query_trades())
    elif args.command in TRADE_QUERY_COMMANDS:
        spec = TRADE_QUERY_COMMANDS[args.command]
        _print_json(gateway.call_trader(spec["method"], with_account=spec["with_account"]))
    elif args.command == "buy":
        _print_json(gateway.submit_order(args.symbol, "BUY", args.volume, args.price))
    elif args.command == "sell":
        _print_json(gateway.submit_order(args.symbol, "SELL", args.volume, args.price))
    elif args.command == "cancel":
        _print_json(gateway.cancel_order(args.order_id))
    elif args.command == "trade-call":
        _print_json(
            gateway.call_trader(
                args.method,
                *json.loads(args.args),
                with_account=not args.no_account,
                **json.loads(args.kwargs),
            )
        )
    return 0


