def _scrub(obj: Any) -> Any:
    """Recursively replace NaN/NaT/NA leaves with None so json.dumps emits valid JSON null.

    json's own encoder happily emits the non-standard ``NaN`` token for float('nan') and never
    calls ``default`` for it, so this has to run before the value reaches json.dumps.
    """
    if isinstance(obj, dict):
        return {key: _scrub(item) for key, item in obj.items()}
    if isinstance(obj, list):
        return [_scrub(item) for item in obj]
    if _pd is not None and (obj is _pd.NaT or obj is _pd.NA):
        return None
    if isinstance(obj, float) and obj != obj:  # NaN is the only float unequal to itself
        return None
    return obj


def _json_default(value: Any) -> Any:
    if _pd is not None and isinstance(value, _pd.DataFrame):
        if isinstance(value.index, _pd.RangeIndex):
            records = value.to_dict(orient="records")
        else:
            # Keep a non-trivial index (commonly a time index) as a plain column instead of
            # dropping it.
            records = value.reset_index().to_dict(orient="records")
        return _scrub(records)
    if _pd is not None and isinstance(value, _pd.Series):
        return _scrub(value.to_dict())
    if _pd is not None and isinstance(value, _pd.Timestamp):
        return value.isoformat()
    if _np is not None and isinstance(value, _np.ndarray):
        return value.tolist()
    if _np is not None and isinstance(value, _np.generic):
        return value.item()
    if is_dataclass(value):
        return asdict(value)
    if hasattr(value, "__dict__"):
        return value.__dict__
    if type(value).__module__.split(".")[0] == "xtquant":
        # xtpythonclient pyd objects (XtAccountInfo, ...) expose plain attributes but
        # have no __dict__; scrape the public, non-callable ones.
        return {
            name: attr
            for name in dir(value)
            if not name.startswith("_")
            for attr in [getattr(value, name, None)]
            if not callable(attr)
        }
    return str(value)


_print_lock = threading.Lock()


def _emit(obj: Any) -> None:
    """Thread-safe single-line JSON print used by the server loop and subscription callbacks."""
    with _print_lock:
        print(json.dumps(obj, ensure_ascii=False, default=_json_default), flush=True)


def _print_json(value: Any) -> None:
    print(json.dumps(value, ensure_ascii=False, default=_json_default, indent=2))


_XTDATA_FIELDS_CACHE: dict[str, Any] | None = None


def _load_xtdata_fields() -> dict[str, Any]:
    """Load the static xtdata field-name dictionaries generated by scripts/extract_doc_fields.py.

    Cached after the first successful load. Falls back to an empty catalog (rather than raising)
    if the generated JSON is missing or unreadable, so ``build_parser()`` never crashes just
    because ``fields`` lost its data file; ``fields`` dispatch reports a clear error instead.
    """
    global _XTDATA_FIELDS_CACHE
    if _XTDATA_FIELDS_CACHE is None:
        try:
            raw = resources.files("qmtcli").joinpath("xtdata_fields.json").read_text(encoding="utf-8")
            _XTDATA_FIELDS_CACHE = json.loads(raw)
        except (FileNotFoundError, OSError, json.JSONDecodeError):
            _XTDATA_FIELDS_CACHE = {}
    return _XTDATA_FIELDS_CACHE


def _dispatch_fields_command(kind: str | None) -> Any:
    catalog = _load_xtdata_fields()
    if not catalog:
        raise ValueError("fields catalog is unavailable; run scripts/extract_doc_fields.py")
    if not kind:
        return {name: entry["title"] for name, entry in catalog.items()}
    if kind not in catalog:
        raise ValueError(f"unknown fields kind: {kind}; choices: {sorted(catalog)}")
    return catalog[kind]["fields"]


